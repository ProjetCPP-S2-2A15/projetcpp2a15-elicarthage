bool MainWindow::estTexteValide(const QString &texte)
{
    if (texte.length() < 3) {
        return false;
    }

    // Expression régulière acceptant des lettres et des espaces
    static QRegularExpression regex("^[A-Za-z\\s]+$");

    return regex.match(texte).hasMatch();
}

