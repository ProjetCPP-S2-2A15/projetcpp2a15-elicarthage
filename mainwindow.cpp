void MainWindow::exporterPDF()
{
    QString fileName = QFileDialog::getSaveFileName(this, "Exporter en PDF", "", "Fichiers PDF (*.pdf)");
    if (fileName.isEmpty()) {
        return;
    }

    Architecte architecte;
    QSqlQueryModel *model = architecte.afficher();
    if (!model) {
        QMessageBox::warning(this, "Erreur", "Impossible de récupérer les données.");
        return;
    }

    QPdfWriter writer(fileName);
    writer.setPageSize(QPageSize::A4);
    writer.setPageOrientation(QPageLayout::Landscape);
    writer.setResolution(120);
    QPainter painter(&writer);
    if (!painter.isActive()) {
        QMessageBox::warning(this, "Erreur", "Impossible de créer le PDF.");
        return;
    }

    const int margin = 30;
    const int titleHeight = 50;
    const int headerHeight = 40;
    const int rowHeight = 35;
    const int colCount = 6;
    int colWidths[colCount] = {210, 220, 220, 220, 220, 220};
    const int lineWidth = 1;


    int yPos = margin;
    int xPos = margin;
    // Polices
    QFont titleFont("Arial", 18, QFont::Bold);
    QFont headerFont("Arial", 10, QFont::Bold);
    QFont dataFont("Arial", 10);

    painter.setFont(titleFont);
    painter.drawText(xPos, yPos, writer.width() - 2*margin, titleHeight,
                     Qt::AlignCenter | Qt::AlignVCenter, "LISTE DES ARCHITECTES");
    yPos += titleHeight + 20;
    // Dessin des en-têtes
    painter.setFont(headerFont);
    painter.setPen(QPen(Qt::black, lineWidth));
    painter.setBrush(QColor(240, 240, 240));
    // Dessin du rectangle d'en-tête
    painter.drawRect(xPos, yPos, writer.width() - 2*margin, headerHeight);
    // Texte des en-têtes
    QString headers[colCount] = {"ID", "NOM", "PRÉNOM", "EMAIL", "RÔLE", "H. SUPPL."};
    int currentX = xPos;
    for (int col = 0; col < colCount; ++col) {
        Qt::Alignment align = (col == colCount-1) ? Qt::AlignCenter : Qt::AlignLeft;

        painter.drawText(currentX, yPos,
                         colWidths[col], headerHeight,
                         align | Qt::AlignVCenter, headers[col]);

        if (col < colCount - 1) {
            painter.drawLine(currentX + colWidths[col], yPos,
                             currentX + colWidths[col], yPos + headerHeight);
        }
        currentX += colWidths[col];
    }
    yPos += headerHeight;


    painter.setFont(dataFont);

    for (int row = 0; row < model->rowCount(); ++row) {
        // Alternance des couleurs de fond
        painter.setBrush(row % 2 ? Qt::white : QColor(248, 248, 248));
        painter.setPen(Qt::NoPen);
        painter.drawRect(xPos, yPos, writer.width() - 2*margin, rowHeight);


        painter.setPen(QPen(Qt::black, lineWidth));
        currentX = xPos;
        for (int col = 0; col < colCount; ++col) {
            QString text = model->data(model->index(row, col)).toString();

            if (col == colCount-1) {
                bool ok;
                int hours = text.toInt(&ok);
                text = ok ? QString::number(hours) : "0";
            }

            Qt::Alignment align = (col == colCount-1) ? Qt::AlignCenter : Qt::AlignLeft;

            painter.drawText(currentX, yPos,
                             colWidths[col], rowHeight,
                             align | Qt::AlignVCenter, text);

            if (col < colCount - 1) {
                painter.drawLine(currentX + colWidths[col], yPos,
                                 currentX + colWidths[col], yPos + rowHeight);
            }
            currentX += colWidths[col];
        }


        painter.drawLine(xPos, yPos, currentX, yPos);
        painter.drawLine(xPos, yPos + rowHeight, currentX, yPos + rowHeight);

        yPos += rowHeight;


        if (yPos > writer.height() - margin - rowHeight) {
            writer.newPage();
            yPos = margin;


            painter.setFont(headerFont);
            painter.setPen(QPen(Qt::black, lineWidth));
            painter.setBrush(QColor(240, 240, 240));
            painter.drawRect(xPos, yPos, writer.width() - 2*margin, headerHeight);

            currentX = xPos;
            for (int col = 0; col < colCount; ++col) {
                Qt::Alignment align = (col == colCount-1) ? Qt::AlignCenter : Qt::AlignLeft;
                painter.drawText(currentX, yPos,
                                 colWidths[col], headerHeight,
                                 align | Qt::AlignVCenter, headers[col]);

                if (col < colCount - 1) {
                    painter.drawLine(currentX + colWidths[col], yPos,
                                     currentX + colWidths[col], yPos + headerHeight);
                }
                currentX += colWidths[col];
            }
            yPos += headerHeight;
        }
    }
    painter.end();
    QMessageBox::information(this, "Exportation réussie", "Le fichier PDF a été exporté avec succès.");
}
