bool MainWindow::estTexteValide(const QString &texte)
{
    // Expression régulière pour vérifier si le texte contient uniquement des lettres (majuscule et minuscule)
    QRegularExpression regex("^[A-Za-z]+$");
    return regex.match(texte).hasMatch();
}

void MainWindow::modifierCellule(const QModelIndex &index)
{
    QSqlQueryModel *model = qobject_cast<QSqlQueryModel *>(ui->tableView->model());

    if (model) {
        // Si la colonne est en lecture seule (index 0 ou 7), ne pas permettre la modification
        if (index.column() == 0 || index.column() == 7 ||  index.column() == 8) {
            QMessageBox::warning(this, tr("Modification non autorisée"), tr("Vous ne pouvez pas modifier cette colonne."));
            return;
        }

        QString columnName = model->headerData(index.column(), Qt::Horizontal).toString();
        QString oldValue = model->data(index).toString();

        QString newValue = QInputDialog::getText(this, tr("Modifier la valeur"), tr("Nouvelle valeur :"), QLineEdit::Normal, oldValue);

        if (!newValue.isEmpty() && newValue != oldValue) {
            // Validation du nom de la colonne
            if (columnName.isEmpty() || !isValidColumn(columnName)) {
                QMessageBox::warning(this, tr("Erreur"), tr("Le nom de la colonne est invalide."));
                return;
            }

            // Si la colonne modifiée est "NBR_HEURES_SUPPLEMENTAIRES", valider que la valeur est un nombre
            if (columnName == "NBR_HEURES_SUPPLEMENTAIRES" && !isValidSupplementaryHours(newValue)) {
                QMessageBox::warning(this, tr("Erreur de saisie"), tr("Les heures supplémentaires doivent être un nombre valide."));
                return;
            }

            // Si la colonne est NOM, PRÉNOM ou RÔLE, valider que la valeur est un texte valide
            if ((columnName == "NOM" || columnName == "PRÉNOM" || columnName == "RÔLE") && !estTexteValide(newValue)) {
                QMessageBox::warning(this, tr("Erreur de saisie"), tr("Le champ doit contenir uniquement des lettres."));
                return;
            }

            // Récupération de l'ID de l'enregistrement à modifier
            int id = model->data(model->index(index.row(), 0)).toInt();

            // Appel de la fonction modifier de la classe Architecte pour mettre à jour la base de données
            Architecte etmp;
            bool success = etmp.modifier(id, columnName, newValue); // Appel à la méthode modifier

            if (success) {
                // Si la modification réussit, mettre à jour la cellule dans le modèle
                model->setData(index, newValue);
                // Rafraîchir les données après modification
                ui->tableView->setModel(etmp.afficher());  // Mettre à jour le modèle de la table
            } else {
                QMessageBox::warning(this, tr("Erreur de mise à jour"), tr("La mise à jour a échoué. Veuillez vérifier les données."));
            }
        }
    } else {
        qDebug() << "Le modèle n'est pas de type QSqlQueryModel.";
    }
}

// Fonction pour valider que la colonne est valide
bool MainWindow::isValidColumn(const QString &columnName)
{
    // Liste des colonnes valides
    QStringList validColumns = {"NOM", "PRÉNOM", "EMAIL","RÔLE","MOT_DE_PASSE","NBR_HEURES_SUPPLEMENTAIRES"}; // Remplace par les noms réels de tes colonnes

    return validColumns.contains(columnName);
}


bool MainWindow::isValidSupplementaryHours(const QString &value)
{
    bool isNumber;
    value.toInt(&isNumber);  // Vérifie si la chaîne peut être convertie en entier
    return isNumber;         // Retourne true si c'est un nombre, sinon false
}


