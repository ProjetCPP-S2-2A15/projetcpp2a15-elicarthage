    int MainWindow::generateAutoID() {
    int autoGeneratedID;
    Architecte A;
    do {
        int randomPart = QRandomGenerator::global()->bounded(1000, 10000);
        // Obtenir la partie basse du timestamp en millisecondes (modulo 10000 pour éviter un ID trop long)
        int timePart = QDateTime::currentMSecsSinceEpoch() % 10000;
        autoGeneratedID = (randomPart * 10) + (timePart % 10);
    } while (A.idExiste(autoGeneratedID));
    return autoGeneratedID;
}
