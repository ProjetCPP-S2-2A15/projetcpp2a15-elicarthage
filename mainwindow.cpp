bool MainWindow::isValidColumn(const QString &columnName)
{

    QStringList validColumns = {"NOM", "PRÉNOM", "EMAIL","RÔLE","MOT_DE_PASSE","NBR_HEURES_SUPPLEMENTAIRES"};

    return validColumns.contains(columnName);
}
//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
bool MainWindow::isValidSupplementaryHours(const QString &value)
{
    bool isNumber;
    value.toInt(&isNumber);
    return isNumber;
}
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
QString MainWindow::hasherMotDePasse(const QString &motDePasse)
{
    QByteArray hash = QCryptographicHash::hash(motDePasse.toUtf8(), QCryptographicHash::Sha256);
    return hash.toBase64();
}
