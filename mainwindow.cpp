void MainWindow::on_btnsupprimer_clicked()
{

    int id = ui->lineEdit_IDS->text().toInt();

    Architecte Etmp;

    if (!Etmp.idExiste(id))
    {

        QMessageBox::warning(this, "Erreur", "ID non trouvé !");
        ui->lineEdit_IDS->clear();
        return;
    }

    QString nom, prenom;
    Etmp.getNomPrenom(id, nom, prenom);

    QString confirmText = "ID: " + QString::number(id) + "\nNom: " + nom + "\nPrénom: " + prenom + "\nSouhaitez-vous vraiment supprimer cet élément ?";


    QMessageBox::StandardButton reply;
    reply = QMessageBox::question(this, "Confirmation", confirmText,
                                  QMessageBox::Yes | QMessageBox::No);


    if (reply == QMessageBox::Yes) {

        bool test = Etmp.supprimer(id);


        if (test)
        {
            qDebug() << "Suppression effectuée avec succès!";


            QSqlQueryModel *model = Etmp.afficher();
            ui->tableView->setModel(model);
        }
        else
        {

            qDebug() << "Erreur: Suppression non effectuée.";
        }
    }
    else
    {

        QMessageBox::information(this, "Annulation", "La suppression a été annulée.");
    }

    // Effacer le champ de texte après l'opération
    ui->lineEdit_IDS->clear();
}

