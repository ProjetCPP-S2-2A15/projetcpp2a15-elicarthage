bool MainWindow::verifierMotDePasse(const QString &motDePasse)
{
    static QRegularExpression regex("^(?=.*[A-Z])(?=.*[a-z])(?=.*\\d)(?=.*[^A-Za-z0-9\\s]).{8,}$");
    QRegularExpressionMatch match = regex.match(motDePasse);

    QLabel *labelErreur = ui->labelErreur;

    if (match.hasMatch()) {
        labelErreur->setText("Mot de passe valide.");
        labelErreur->setStyleSheet("color: green;");

        return true;
    } else {
        labelErreur->setText("Mot de passe:Min.8 caractères,1majuscule,<br>1 minuscule,1 chiffre,1 spécial.");
        labelErreur->setStyleSheet("color: red;");
        return false;
    }
}



bool MainWindow::validateEmail(const QString &email, bool isModification)
{
    static QRegularExpression emailRegex("^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$");
    QRegularExpressionMatch match = emailRegex.match(email);
    bool valid = match.hasMatch();

    if (!isModification) {
        QLabel *labelEmailStatus = ui->labelEmailStatus;

        if (valid) {
            labelEmailStatus->setText("Email valide");
            labelEmailStatus->setStyleSheet("color: green; qproperty-alignment: AlignCenter;");
            labelEmailStatus->setAlignment(Qt::AlignCenter);
        } else {
            labelEmailStatus->setText("Email invalide");
            labelEmailStatus->setStyleSheet("color: red; qproperty-alignment: AlignCenter;");
            labelEmailStatus->setAlignment(Qt::AlignCenter);
        }
    }

    return valid;
}




bool MainWindow::verifierChampsLettresSeules(const QString &champ, QLabel *labelErreur)
{
    if (champ.length() < 3) {
        labelErreur->setText("Invalide: Min. 3 caractères");
        labelErreur->setStyleSheet("color: red; qproperty-alignment: AlignCenter;");
        return false;
    }

    // Expression régulière acceptant des lettres et des espaces
    static QRegularExpression regex("^[A-Za-z\\s]+$");

    // Déclarer QRegularExpressionMatch dans la même portée
    QRegularExpressionMatch match = regex.match(champ);

    if (match.hasMatch()) {
        labelErreur->setText("Valide");
        labelErreur->setStyleSheet("color: green; qproperty-alignment: AlignCenter;");
        return true;
    } else {
        labelErreur->setText("Invalide: uniquement des lettres et espaces.");
        labelErreur->setStyleSheet("color: red; qproperty-alignment: AlignCenter;");
        return false;
    }
}


